#include <stdio.h>

int ft_sqrt(int nb)
{
    int a;

	a = 1;
    while (nb != (a * a))
    {
        a++;
    }
    return (a);
}

int	main(void)
{
	printf("%i\n", ft_sqrt(25));
	return (0);
}
