#include <stdio.h>
#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	e;

	p = 'P';
	e = 'N';
	if (n >= 0)
		write(1, &p, 1);
	else
		write(1, &e, 1);
}
